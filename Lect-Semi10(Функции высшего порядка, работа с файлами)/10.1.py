data = [1, 2, 3, 5, 8, 15, 23, 38]

# Используем map для применения функции ко всем элементам коллекции
res = list(map(int, data))

# Используем filter для фильтрации элементов коллекции по условию
res = list(filter(lambda x: x % 2 == 0, res))

print(res)  # [2, 8, 38]

# Используем map для применения функции ко всем элементам коллекции
res = list(map(lambda x: (x, x ** 2), res))

print(res)  # [(2, 4), (8, 64), (38, 1444)]
