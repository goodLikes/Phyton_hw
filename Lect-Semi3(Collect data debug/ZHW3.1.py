""" Требуется найти в массиве list_1 самый близкий по значению элемент к заданному числу k и вывести его.
Считать, что такой элемент может быть только один. Если значение k совпадает с этим элементом - выведите его.

Пример:
list_1 = [1, 2, 3, 4, 5]
k = 6
# 5 """

list_1 = [1, 2, 3, 4, 5]
k = 4.51

# Инициализируем переменные для хранения ближайшего значения и минимальной разности
closest = list_1[0]
min_diff = abs(list_1[0] - k)

# Проходим по каждому элементу списка
for num in list_1:
    diff = abs(num - k)
    # Если текущая разность меньше, чем минимальная найденная до сих пор,
    # обновляем ближайшее значение и минимальную разность
    if diff < min_diff:
        closest = num
        min_diff = diff

print(closest)